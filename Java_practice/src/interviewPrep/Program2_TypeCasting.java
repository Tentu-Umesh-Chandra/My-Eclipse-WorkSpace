package interviewPrep;

public class Program2_TypeCasting {

	public static void main(String[] args) {
		//IMPLICIT TYPE CASTING
		int num = 10;
		double marks = num;
		System.out.println(marks);
		
		//EXPLICIT TYPE CASTING
		double data2 = 98.5f;
		int num2 = (int)data2;
		System.out.println(num2);
	}

}
